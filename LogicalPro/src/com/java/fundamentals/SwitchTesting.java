package com.java.fundamentals;

public class SwitchTesting {

	public static void main(String[] args) {
		int x = 1;
		switch(x)
		{
		case 0:
			System.out.println("0 stmt");
			break;
		case 1:
			System.out.println("1 stmt");
		case 2:
			System.out.println("2 stmt");
		default:
			System.out.println("default stmt");
			
		}
	}

}
