package com.dsa.basics.Jan9TH;
//https://codeforwin.org/c-programming/square-star-pattern-in-c
public class Pattern_program_5 {

	public static void main(String[] args) {
		for(int i = 1 ; i<=5 ; i++) {
			for(int j = 1; j<=5; j++) {
				System.out.print("*");
			}
			System.out.println("*");
		}
	}

}
