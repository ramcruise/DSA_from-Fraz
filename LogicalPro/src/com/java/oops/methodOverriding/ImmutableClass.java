package com.java.oops.methodOverriding;

public final class ImmutableClass {
	private int id;
	private String name;
	
	public ImmutableClass(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	

}
