import java.util.Scanner;

//Given 3 sides of a triangle, check whether it's a valid triangle or not?
public class Valid_Triangle_3 {

	public static void main(String[] args) {
		int a,b,c,sum;
		int default_sum = 180;
		boolean flag = false;
		Scanner s = new Scanner(System.in);
		System.out.println("Enter the angle of a ");
		 a = s.nextInt();
		System.out.println("Enter the angle of b ");
		 b = s.nextInt();
		System.out.println("Enter the angle of c ");
		 c = s.nextInt();
		
		if(a!=0 || b!=0 || c!=0) {
			sum = a+b+c;
			if(default_sum != sum) {
				System.out.println("Given 3 sides are a triangle is a valid : "+flag);
			}else {
				flag = true;
				System.out.println("Given 3 sides are a triangle is a valid : "+flag);
			}
			
		}
		
	}

}
